#pragma once

#ifndef WEB_COMMON_LIB_HTTP_HTTP_HEADER_H_
#define WEB_COMMON_LIB_HTTP_HTTP_HEADER_H_

#include <unordered_map>
#include <string>

/**
 * @file http_header.h
 * @brief Определение класса HttpHeader для безопасной и регистронезависимой работы с заголовками HTTP.
 */

namespace web
{
namespace http
{

/**
 * @class HttpHeader
 * @brief Класс-контейнер для хранения, валидации и обработки HTTP-заголовков (метаданных).
 * 
 * Автоматически нормализует имена заголовков (приводит к нижнему регистру) для обеспечения 
 * регистронезависимого поиска в соответствии со стандартами RFC (включая HTTP/1.1, HTTP/2 и HTTP/3).
 * Также очищает значения заголовков от лишних сетевых символов переноса строк и пробелов (Trim).
 */
class HttpHeader
{
private:
    /**
     * @brief Внутренний метод для нормализации имени заголовка.
     * 
     * Удаляет окружающие пробельные символы (s,t,r,n) и приводит строку к нижнему регистру.
     * @param name Сырое имя заголовка из сети или пользовательского кода.
     * @return Очищенная строка в нижнем регистре. Если имя некорректно — пустая строка.
     */
    std::string NormalizeName(const std::string& name) const;

    /**
     * @brief Внутренний метод для очистки значения заголовка (Trim).
     * 
     * Удаляет пробелы, знаки табуляции и символы перевода строк (r,n) с обоих концов значения.
     * @param value Сырое значение заголовка из сетевого пакета.
     * @return Очищенная строка значения.
     */
    std::string NormalizeValue(const std::string& value) const;

public:
    /**
     * @brief Добавляет заголовок или перезаписывает существующий.
     * 
     * Имя и значение автоматически проходят нормализацию перед сохранением.
     * @param name Имя заголовка (например, "Content-Type" или "  host ").
     * @param value Значение заголовка (например, "application/jsonrn").
     */
    void Set(const std::string& name, const std::string& value);

    /**
     * @brief Извлекает значение заголовка по его имени.
     * 
     * Имя нормализуется автоматически, поэтому поиск регистронезависим.
     * Метод потокобезопасен для одновременного чтения из параллельных потоков.
     * @param name Имя запрашиваемого заголовка.
     * @return Строка со значением заголовка. Если заголовок не найден — пустая строка.
     */
    std::string Get(const std::string& name) const;

    /**
     * @brief Проверяет наличие заголовка в контейнере.
     * 
     * Поиск выполняется в стиле C++17 (через find) и является регистронезависимым.
     * @param name Имя проверяемого заголовка.
     * @return true, если заголовок присутствует; false, если отсутствует или имя невалидно.
     */
    bool Has(const std::string& name) const;

    /**
     * @brief Удаляет конкретный заголовок из контейнера.
     * @param name Имя удаляемого заголовка (регистр не важен).
     */
    void Remove(const std::string& name);

    /**
     * @brief Полностью очищает контейнер, удаляя все сохраненные заголовки.
     */
    void Clear();

private:
    /**
     * @brief Внутреннее хранилище заголовков.
     * Ключи всегда хранятся исключительно в нижнем регистре без пробелов.
     */
    std::unordered_map<std::string, std::string> headers_;
};

} // namespace http
} // namespace web

#endif // WEB_COMMON_LIB_HTTP_HTTP_HEADER_H_
